'''
+,-  Soma e subtração
*, /, //  Vezes, Dividido, Dividido arredondado pra float
**, %, ()  Exponencial, Resto de uma divisão, Precêdencia de ordem
'''
print('ADIÇÃO', 10+10)
print('SUBTRAÇÃO',10-10)
print('MULTIPLICAÇÃO',10*10)
print('DIVISÃO',10/10)
print('DIVISÃO ARREDONDADA',10//4)
print('EXPONECIAL',10**2)
print('RESTO DE UMA DIVISÃO',10%2)



